	public List<Album> getVertici(double durata, Map<Integer, Album> mappa ){
		final String sql = "SELECT SUM(t.Milliseconds) as d, t.AlbumId "
				+ "FROM track t "
				+ "GROUP BY t.AlbumId "
				+ "HAVING SUM(t.Milliseconds) > ?";
		
		List<Album> result = new LinkedList<>();
		
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			st.setDouble(1, durata);
			ResultSet res = st.executeQuery();

			while (res.next()) {
				Album a = new Album(res.getInt("AlbumId"), mappa.get(res.getInt("AlbumId")).getTitle() ,res.getDouble("d")/60000) ;
				result.add(a);
			}
			conn.close();
		} catch (SQLException e) {
			e.printStackTrace();
			throw new RuntimeException("SQL Error");
		}
		return result;
	}
	
	public List<Coppie> getCoppie(Map <Integer, Album>mappa ){
		String sql= "SELECT DISTINCTROW a1.AlbumId as id1, a2.AlbumId as id2 "
				+ "FROM album a1, album a2, track t1, track t2, playlisttrack p1, playlisttrack p2 "
				+ "WHERE a1.AlbumId = t1.AlbumId AND t2.AlbumId= a2.AlbumId AND p1.TrackId = t1.TrackId AND p2.TrackId = t2.TrackId AND p1.PlaylistId=p2.PlaylistId";
		List<Coppie> result = new ArrayList<>();
		
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			ResultSet res = st.executeQuery();

			while (res.next()) {
				Coppie c = new Coppie(mappa.get(res.getInt("id1")),mappa.get(res.getInt("id2")));
				result.add(c);
			}
			conn.close();
			return result;
		} catch (SQLException e) {
			e.printStackTrace();
			throw new RuntimeException("SQL Error");
		}
	}
